// src/core/ResonantSignal.ts
// Единый формат сигнала, который циркулирует по организму LIMINAL

export interface ResonantSignal {
  /**
   * Имя слоя-источника, например:
   * "L21", "L22", "L23", "L24", "Core"
   */
  sourceLayer: string;

  /**
   * Кому адресован сигнал.
   * - конкретное имя силы (например "L21-Luck")
   * - "ALL" — всем силам
   * - null — не адресовано никому конкретно (широковещательный)
   */
  target: string | null;

  /**
   * Амплитуда импульса (0..1 и выше).
   * Показывает "силу волны" — насколько сильно это решение/событие.
   */
  amplitude: number;

  /**
   * Смещение вероятности (от -1 до +1).
   * Положительное — усиливает вероятность "удачного" исхода,
   * отрицательное — может сигнализировать о риске/снижении шансов.
   */
  probabilityShift: number;

  /**
   * Временная метка генерации сигнала.
   */
  timestamp: number;

  /**
   * Дополнительные данные (контекст решения, id опции,
   * оценки рисков/резонанса и т.д.)
   */
  metadata?: Record<string, any>;
}
